def coco_plus_resize(images, boxes, transforms):
    """
    absolute xywh -> relative xyxy
    """
    # width, height = images[0].size 
    image = images[0]
    boxes = BoxUtil.box_convert(boxes, "xywh", "xyxy")
    # boxes = BoxUtil.scale_bounding_box(
    #     boxes, width, height, mode="down"
    # )
    image, boxes = transforms(image, boxes)
    boxes = BoxUtil.box_convert(boxes, 'xyxy', 'cxcywh')
    h, w = image.size
    boxes = boxes / torch.tensor([w, h, w, h], dtype=torch.float32)

    return [image], boxes


